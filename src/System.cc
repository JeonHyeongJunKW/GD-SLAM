//static_cast : 실수와 정수, 열거형과 정수형, 실수와 실수 사이의 변환을 허용하지만, 포인터는 상속관계면 가능 static_cast<자식클래스>(부모클래스)
System::System(const string &strVocFile, const string &strSettingsFile, const eSensor sensor,
               const bool bUseViewer):mSensor(sensor), mpViewer(static_cast<Viewer*>(NULL)), mbReset(false),mbActivateLocalizationMode(false),
        mbDeactivateLocalizationMode(false)//각 변수에 대한 할당으로 사용한다.(센서의 )
{
    // Output welcome message
    cout << endl <<
    "DynaSLAM Copyright (C) 2018 Berta Bescos, University of Zaragoza." << endl << endl;
          
    cout << "Input sensor was set to: ";

    if(mSensor==MONOCULAR)
        cout << "Monocular" << endl;
    else if(mSensor==STEREO)
        cout << "Stereo" << endl;
    else if(mSensor==RGBD)
        cout << "RGB-D" << endl;

    //Check settings file
    cv::FileStorage fsSettings(strSettingsFile.c_str(), cv::FileStorage::READ);
    if(!fsSettings.isOpened())
    {
       cerr << "Failed to open settings file at: " << strSettingsFile << endl;
       exit(-1);
    }


    //Load ORB Vocabulary
    cout << endl << "Loading ORB Vocabulary. This could take a while..." << endl;

    mpVocabulary = new ORBVocabulary();//여기서부터는 DBOW2의 라이브러리를 사용한것
    bool bVocLoad = mpVocabulary->loadFromTextFile(strVocFile);// vocabulary파일을 읽어온다.
    if(!bVocLoad)
    {
        cerr << "Wrong path to vocabulary. " << endl;
        cerr << "Falied to open at: " << strVocFile << endl;
        exit(-1);
    }
    cout << "Vocabulary loaded!" << endl << endl;

    //Create KeyFrame Database
    mpKeyFrameDatabase = new KeyFrameDatabase(*mpVocabulary);//키프레임을 저장하는 데이터베이스 선언
    //Create the Map
    mpMap = new Map();//맵포인트를 저장하는 변수 선언

    //Create Drawers. These are used by the Viewer
    mpFrameDrawer = new FrameDrawer(mpMap);//프레임를 시각화해주는 변수 선언
    mpMapDrawer = new MapDrawer(mpMap, strSettingsFile);//맵포인트를 그려주는 변수 선업

    //Initialize the Tracking thread
    //(it will live in the main thread of execution, the one that called this constructor)
    //트랙킹 쓰레드, voabulary, frame drawer, map drawer, 맵의 포인터, 키프레임 데이터 저장, 셋팅 파일 경로, 센서 종류
    mpTracker = new Tracking(this, mpVocabulary, mpFrameDrawer, mpMapDrawer,
                             mpMap, mpKeyFrameDatabase, strSettingsFile, mSensor);
    
    //Initialize the Local Mapping thread and launch
    mpLocalMapper = new LocalMapping(mpMap, mSensor==MONOCULAR);//로컬매핑의 데이터와 관련된 데이터이다.
    mptLocalMapping = new thread(&ORB_SLAM2::LocalMapping::Run,mpLocalMapper);//로컬매핑해주는 쓰레드함수 포인트는 ORB_SLAM2::의형태로 전역으로 참조하는 것
    
    //Initialize the Loop Closing thread and launch
    //loop closing이며, 센서의 종류, 맵포인트 데이터, 키프레임 데이터, vocabulary 데이터를 인자로 준다.
    mpLoopCloser = new LoopClosing(mpMap, mpKeyFrameDatabase, mpVocabulary, mSensor!=MONOCULAR);
    mptLoopClosing = new thread(&ORB_SLAM2::LoopClosing::Run, mpLoopCloser);//런이라는 함수를 준다.
    
    //Initialize the Viewer thread and launch
    if(bUseViewer)
    {
        mpViewer = new Viewer(this, mpFrameDrawer,mpMapDrawer,mpTracker,strSettingsFile);
        
        mptViewer = new thread(&Viewer::Run, mpViewer);// 함수의 포인터를 반환한다.
        
        mpTracker->SetViewer(mpViewer);//트랙커에 viewer세팅한다.
    }
    //Set pointers between threads 각 공유 변수에 대해서 다른 공유 변수의 정보를 제공한다.
    mpTracker->SetLocalMapper(mpLocalMapper);
    mpTracker->SetLoopClosing(mpLoopCloser);

    mpLocalMapper->SetTracker(mpTracker);
    mpLocalMapper->SetLoopCloser(mpLoopCloser);

    mpLoopCloser->SetTracker(mpTracker);
    mpLoopCloser->SetLocalMapper(mpLocalMapper);
}

cv::Mat System::TrackStereo(const cv::Mat &imLeft, const cv::Mat &imRight, const cv::Mat &maskLeft, const cv::Mat &maskRight,const double &timestamp)
{
    //stereo에 대해서 트랙킹하는 함수 
    if(mSensor!=STEREO)
    {
        cerr << "ERROR: you called TrackStereo but input sensor was not set to STEREO." << endl;
        exit(-1);
    }   

    // Check mode change
    {
        unique_lock<mutex> lock(mMutexMode);//모드 바꾸는 플래그
        if(mbActivateLocalizationMode)//위치 추정모드일 때
        {
            mpLocalMapper->RequestStop();//로컬맵핑 일시 중지 요청

            // Wait until Local Mapping has effectively stopped
            while(!mpLocalMapper->isStopped())//로컬매핑 중지까지 기다림
            {
                usleep(1000);
            }

            mpTracker->InformOnlyTracking(true);//트랙킹요청
            mbActivateLocalizationMode = false;//로컬라이징 모드 해제
        }
        if(mbDeactivateLocalizationMode)//위치추정모드가 아닐 때
        {
            mpTracker->InformOnlyTracking(false);//트랙킹만 하는 모드가 아니다.
            mpLocalMapper->Release();//매핑머신이 쉬게해주는듯하다.
            mbDeactivateLocalizationMode = false;
        }
    }

    // Check reset
    {
    unique_lock<mutex> lock(mMutexReset);//리셋 플레그
    if(mbReset)//리셋단계인지 확인한다.
    {
        mpTracker->Reset();//모든걸 리셋한다.
        mbReset = false;
    }
    }

    //카메라 포즈를 얻어온다.
    cv::Mat Tcw = mpTracker->GrabImageStereo(imLeft,imRight, maskLeft, maskRight, timestamp);

    unique_lock<mutex> lock2(mMutexState);
    mTrackingState = mpTracker->mState;//현재 상태를 얻어온다.
    mTrackedMapPoints = mpTracker->mCurrentFrame.mvpMapPoints;//현재 프레임의 맵포인트를 얻어온다.
    mTrackedKeyPointsUn = mpTracker->mCurrentFrame.mvKeysUn;//키포인트를 얻어온다.
    return Tcw;
}
